package qpses.business;

public class ExcelCell {
	private int sheet = 0;
	private int row = 0;
	private int cell = 0;
	private String value = null;
	private int isInteger = 0;
	
	/**
	 * Constructor for ExcelCell
	 */
	public ExcelCell() {}
	
	public int getSheet() {
		return sheet;
	}
	public void setSheet(int sheet) {
		this.sheet = sheet;
	}
	public int getRow() {
		return row;
	}
	public void setRow(int row) {
		this.row = row;
	}
	public int getCell() {
		return cell;
	}
	public void setCell(int cell) {
		this.cell = cell;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public int isInteger() {
		return isInteger;
	}
	public void setInteger(int isInteger) {
		this.isInteger = isInteger;
	}
}
